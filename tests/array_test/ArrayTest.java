public class ArrayTest {
  public static void main(String[] args) {
    boolean[] za = new boolean[1];
    za[0] = true;
    System.out.println(za[0]);

    char[] ca = new char[1];
    ca[0] = 1;
    System.out.println(ca[0]);

    byte[] ba = new byte[1];
    ba[0] = 1;
    System.out.println(ba[0]);

    short[] sa = new short[1];
    sa[0] = 1;
    System.out.println(sa[0]);


    int[] ia = new int[1];
    ia[0] = 1;
    System.out.println(ia[0]);

    long[] ja = new long[1];
    ja[0] = 1L;
    System.out.println(ja[0]);

    float[] fa = new float[1];
    fa[0] = 1f;
    System.out.println(fa[0]);

    double[] da = new double[1];
    da[0] = 1d;
    System.out.println(da[0]);
  }
}